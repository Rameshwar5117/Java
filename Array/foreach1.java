public class foreach1 {
    public static void main(String args[]) {
        int arr[] = { 10, 20, 30, 40, 50 };
        for (int i : arr) {
            System.out.println(i);
        }

        String str[] = { "rameshwar", "Gopal", "jivan" };
        for (String string : str) {
            System.out.println(string);
        }
    }
}
